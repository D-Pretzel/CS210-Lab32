int factorial(int N) {

    if (N < 2) {
        return 1;
    }

    return N * factorial(N-1);
}

int tail_factorial(int N, int accum) {

    if (N < 2) {
        return accum;
    } else {
        return tail_factorial(N-1, N*accum);
    }
}

